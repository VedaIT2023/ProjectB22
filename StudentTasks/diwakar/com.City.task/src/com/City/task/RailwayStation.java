package com.City.task;

public class RailwayStation {
		String name;
		int platforms;
		int shops;
		int waitinghalls;
		int lights;
		int fans;
		
			RailwayStation(String name,int platforms,int shops,int waitinghalls,int lights,int fans){
				this.name=name;
				this.platforms=platforms;
				this.shops=shops;
				this.waitinghalls=waitinghalls;
				this.lights=lights;
				this.fans=fans;
			}
		
}
