package com.vedait.inheritance.city;

public class College {
	String name;
	int floor;
	int classes;
	int teacher;
	int student;
	int lights;
	int fans;
	
	College(String name,int floor,int classes,int teacher,int student,int lights,int fans){
		this.name=name;
		this.floor=floor;
		this.classes=classes;
		this.teacher=teacher;
		this.student=student;
		this.lights=lights;
		this.fans=fans;
	}
}
