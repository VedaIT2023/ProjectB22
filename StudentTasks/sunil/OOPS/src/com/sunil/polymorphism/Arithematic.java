package com.sunil.polymorphism;

public class Arithematic {

	static void  additionOfNumbers(int a,int b) {
		int sum=a+b;
		System.out.println("Addition of  two numbers : "+sum);
	}
	static void  additionOfNumbers(int a,int b,int c) {
		int sum=a+b+c;
		System.out.println("Addition of  Three nnumbers : "+sum);
	}
	static void  additionOfNumbers(int a,int b,int c,int  d) {
		int sum=a+b+c+d;
		System.out.println("Addition of  Four nnumbers : "+sum);
	}
	static void  additionOfNumbers(int a,int b,int c,int  d,int e) {
		int sum=a+b+c+d+e;
		System.out.println("Addition of  Five nnumbers : "+sum);
	}
	void  subtractionOfNumbers(int a,int b) {
		int sub=a-b;
		System.out.println("Subtraction of  Two numbers : "+sub);
	}
	void  subtractionOfNumbers(int a,int b,int c) {
		int sub=a-b-c;
		System.out.println("Subtraction of  Three numbers : "+sub);
	}
	void  subtractionOfNumbers(int a,int b,int c,int  d) {
		int sub=a-b-c-d;
		System.out.println("Subtraction of  Four numbers : "+sub);
	}
	void  subtractionOfNumbers(int a,int b,int c,int d,int e) {
		int sub=a-b-c-d-e;
		System.out.println("Subtraction of  Five numbers : "+sub);
	}
	static void  multiplicationOfNumbers(int a,int b) {
		int mul=a*b;
		System.out.println("multiplication of  Two numbers : "+mul);
	}
	static void  multiplicationOfNumbers(int a,int b,int c) {
		int mul=a*b*c;
		System.out.println("multiplication of  Three numbers : "+mul);
	}
	static void  multiplicationOfNumbers(int a,int b,int c,int  d) {
		int mul=a*b*c*d;
		System.out.println("multiplication of  Four numbers : "+mul);
	}
	static void  multiplicationOfNumbers(int a,int b,int c,int d,int e) {
		int mul=a*b*c*d*e;
		System.out.println("multiplication of  Five numbers : "+mul);
	}

	

}