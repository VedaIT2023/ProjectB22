package com.vedait.student;

public class Student {
	String name;
	String email;
	int age;
	String phoneNumber;
	char gender;
	int studentId;
	
	
	Student(String name,String email,int age,String phoneNumber,char gender,
			int studentId){
		this.name=name;
		this.email=email;
		this.age=age;
		this.phoneNumber=phoneNumber;
		this.gender=gender;
		this.studentId=studentId;
		
	}

}
