public class ArithmeticOperators{
	public static void main(String[] args){
		int num1=12,num2=18,add,subtract,multiplicate;
		double divide,modulus;
		add = num1+num2;
		subtract = num1-num2;
		multiplicate = num1*num2;
		divide = (double)num1/(double)num2;
		modulus = (double)num1%(double)num2;
	System.out.println("Addition is : "+add);
	System.out.println("Subtraction is : "+subtract);
	System.out.println("Multiplication is : "+multiplicate);
	System.out.println("division is : "+divide);
	System.out.println("percentile is : "+modulus);
	}
}





