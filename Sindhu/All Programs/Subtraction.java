public class Subtraction{
	public static void main(String[] args){
		int num1=46,num2=14,num3,num4,num5;		
		num3 = num1-num2;
		num4 = num1-num2-num3;
		num5 = num1-num2-num3-num4;
	System.out.println("Sub of 2 numbers : "+num3);
	System.out.println("Sub of 3 numbers : "+num4); 
	System.out.println("Sub of 4 numbers : "+num5); 
	}
}