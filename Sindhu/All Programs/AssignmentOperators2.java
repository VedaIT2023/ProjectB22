public class AssignmentOperators2{
	public static void main(String[] args){
		//AssignmentOperators : = , += , -= , *= , /= , %=;
		int age1=46;
		age1+=5;              //age1=age+5
	System.out.println("Age is "+age1);
		int age2=46;
		age2-=5;            //age2=age2-5
	System.out.println("Age is "+age2);
		int age3=46;
		age3*=5;
	System.out.println("Age is "+age3);
		float age4=46;
		age4/=5;
	System.out.println("Age is "+age4);
		float age5=46;
		age5%=5;
	System.out.println("Age is "+age5);

	}
}