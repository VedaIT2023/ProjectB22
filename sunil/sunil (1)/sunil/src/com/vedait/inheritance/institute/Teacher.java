package com.vedait.inheritance.institute;

public class Teacher {
	String name,email,subject,gender;
	long phoneNumber;
	int experience;
	
	Teacher(String name,String email,String subject,String gender,long phoneNumber,int experience){
		this.name=name;
		this.email=email;
		this.subject=subject;
		this.gender=gender;
		this.phoneNumber=phoneNumber;
		this.experience=experience;
	}

}
